export async function POST(request: Request) {
  try {
    console.log('📨 内容生成 API 收到请求')
    
    const { content, title, platform, customPrompt, stream } = await request.json()
    console.log('📝 请求数据:', { platform, title, contentLength: content?.length, hasCustomPrompt: !!customPrompt, stream })
    
    if (!content) {
      return NextResponse.json({ error: '笔记内容为空' }, { status: 400 })
    }
    
    // 特殊处理自定义平台
    let prompt, systemPrompt;
    if (platform === 'custom' && customPrompt) {
      const titleLine = title ? `标题：${title}\n` : '';
      prompt = `${customPrompt}\n\n笔记内容：\n${titleLine}\n${content.substring(0, 3000)}`;
      systemPrompt = '你是一个专业的内容改写助手。';
    } else {
      // 获取平台提示词配置
      const config = getPlatformPrompts(title || '', content)[platform];
      
      if (!config) {
        return NextResponse.json({ error: '不支持的平台' }, { status: 400 });
      }
      
      prompt = config.prompt;
      systemPrompt = config.systemPrompt;
    }
    
    // 如果请求需要流式响应
    if (stream) {
      const { callAIStream } = await import('@/lib/ai/stream')
      const aiStream = callAIStream(prompt, systemPrompt)

      return new Response(aiStream, {
        headers: {
          'Content-Type': 'text/event-stream',
          'Cache-Control': 'no-cache',
          'Connection': 'keep-alive',
        }
      })
    }
    
    console.log('🤖 开始调用 AI...')
    const result = await callAI(prompt, systemPrompt)
    console.log('✅ AI 调用成功，长度:', result?.length)
    
    if (!result) {
      return NextResponse.json({ error: 'AI 返回空结果' }, { status: 500 })
    }
    
    return NextResponse.json({ result })
  } catch (error) {
    console.error('❌ 内容生成 API 错误:', error)
    return NextResponse.json({ 
      error: error instanceof Error ? error.message : '服务器内部错误'
    }, { status: 500 })
  }
}
